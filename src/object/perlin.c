static float __attribute__ ((pure))	dot3(const char a[],
					     float x,
					     float y,
					     float z)
{
  return (a[0] * x + a[1] * y + a[2] * z);
}

static float __attribute__ ((const))	lerp(float a, float b, float t)
{
  return ((1 - t) * a + t * b);
}

static float __attribute__ ((const))	fade(float n)
{
  return (n * n * n * (n * (n * 6 - 15) + 10));
}

float __attribute__((pure))	get_perlin2(unsigned int gi[8],
					    sfVector3f r, sfVector3f f)
{
  float				n[8];
  float				nx[4];
  float				nxy[2];
  int				index;

  index = -1;
  while (++index != 8)
    n[index] = dot3(g_grad3[gi[index]], r.x - ((index >> 2) & 1),
		    r.y - ((index >> 1) & 1), r.z - (index & 1));
  f.x = fade(r.x);
  f.y = fade(r.y);
  f.z = fade(r.z);
  index = -1;
  while (++index != 4)
    nx[index] = lerp(n[index], n[4 + index], f.x);
  index = -1;
  while (++index != 2)
    nxy[index] = lerp(nx[index], nx[2 + index], f.y);
  return (lerp(nxy[0], nxy[1], f.z));
}

float __attribute__((pure))	get_perlin(float x, float y, float z)
{
  sfVector3f			g;
  sfVector3f			r;
  sfVector3f			f;
  unsigned int			gi[8];
  int				index;

  g.x = FLOOR(x);
  g.y = FLOOR(y);
  g.z = FLOOR(z);
  r.x = x - g.x;
  r.y = y - g.y;
  r.z = z - g.z;
  g.x = (int)g.x & 255;
  g.y = (int)g.y & 255;
  g.z = (int)g.z & 255;
  index = -1;
  while (++index != 8)
    gi[index] = g_perm[(int)g.x + ((index >> 2) & 1) +
		     g_perm[(int)g.y + ((index >> 1) & 1) +
			  g_perm[(int)g.z + (index & 1)]]] % 12;
  return (get_perlin2(gi, r, f));
}
